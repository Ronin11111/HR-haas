<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">
          <img src="@/assets/common/login-logo.png" alt="">
        </h3>
      </div>

      <el-form-item prop="mobile">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.mobile"
          placeholder="请输入手机号"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="请输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button class="loginBtn" :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>

      <div class="tips">
        <span style="margin-right:20px;">账号: 13800000002</span>
        <span> 密码: 123456</span>
      </div>

    </el-form>
  </div>
</template>

<script>
import { validMobile } from '@/utils/validate'
import { mapActions } from 'vuex'

export default {
  name: 'Login',
  data() {
    const validateMobile = function(rule, value, callback) {
      // 注意：错误结果的callback函数中，需要new Error对象，再来提示消息
      validMobile(value) ? callback() : callback(new Error('手机号格式错误！'))
    }
    return {
      // 将校验表单字段设置为mobile，与接口所需字段对应
      loginForm: {
        mobile: '13800000002',
        password: '888itcast.CN764%...'
      },
      loginRules: {
        mobile: [{ required: true, trigger: 'blur', message: '手机号不能为空' },
          { validator: validateMobile, trigger: 'blur' }],
        password: [{ required: true, trigger: 'blur', message: '密码不能为空' },
          { min: 6, max: 20, trigger: 'blur', message: '密码必须为6-16位' }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    // 将vuex中的方法导出在methods中
    // 语法：mapAction['子组件/方法名']
    ...mapActions(['user/login']),
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      // 在组件上使用ref可获取该组件的实例
      this.$refs.loginForm.validate(async valid => {
        if (valid) {
          try {
            this.loading = true
            // 1.调用vuex中的action,发起请求
            // 发起请求，传递表单数据
            await this['user/login'](this.loginForm)
            // await为强制等待，等待异步操作执行完毕，才执行之后的代码
            this.$router.push('/')
          } catch (error) {
            // 2.无需再添加错误组件提示，在响应拦截器中已统一配置
            console.log(error)
          } finally {
            // 不论执行成功/失败，都会执行finally{}
            this.loading = false
          }
        }
      })
    }
  }
}
</script>

